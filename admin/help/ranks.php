<h2>The Rank System?</h2>
<p>Ranks have been an idea from the beginning but has only been implemented in Version 2.5 SVN28 of View Panel.</p>
<h2>What Do They Do?</h2>
<p>Ranks are supposed to be a way of controlling who can do what around your blog. </p>
<h2>I See Numbers...</h2>
<p>Each rank has a number and each number has a set of permissions attached to it. These are as follows:</p>
<table width="829" border="0">
  <tr>
    <td>Number</td>
    <td>Rank</td>
    <td>Permissions</td>
  </tr>
  <tr>
    <td>1</td>
    <td>Admin</td>
    <td>Can do everything. No limits.</td>
  </tr>
  <tr>
    <td>2</td>
    <td>User  Moderator</td>
    <td>Can log into the admin area and make, edit, delete profiles.</td>
  </tr>
  <tr>
    <td>3</td>
    <td>Post Moderator</td>
    <td>Can log into the admin panel and make, edit, delete posts as well as introductions.</td>
  </tr>
  <tr>
    <td>4</td>
    <td>File Moderator</td>
    <td>Can log into the admin panel and upload, delete files.</td>
  </tr>
  <tr>
    <td>5</td>
    <td>User</td>
    <td>A user can login and edit their own profile as well as upload files.</td>
  </tr>
  <tr>
    <td>6</td>
    <td>Sanctioned User</td>
    <td>A sanctioned user cannot upload files.</td>
  </tr>
</table>
<h2>Can I Edit Permissions?</h2>
<p>No, not yet.</p>
