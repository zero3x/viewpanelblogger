<h1><strong>Bugs And Errors</strong></h1>
<p><a name="q1" id="q1"></a>Q: I've installed View Panel but when I go to login I get an error saying it's not.</p>
<p>A: First off check that the files &quot;installcomplete.txt&quot; &amp; &quot;databasesettings.php&quot; exist in the lib directory. If they do try logging into View Panel. If you can then just ignore the error and tell someone on the forum.</p>
<p>&nbsp;</p>
<p><a name="q2" id="q2"></a>Q: I get errors saying certain files don't exist.</p>
<p>A: This usually means you or a rouge piece of code has deleted a file. Go to the forum and paste the error including what files don't exist and your View Panel version.</p>
<p>&nbsp;</p>
<p><a name="q6" id="q6"></a>Q: I get an access denied message and I haven't turned off / blocked the feature.</p>
<p>A: Then View Panel has a problem please get some help on the <a href="http://forum.StreetEye.info">forum</a>.</p>
<p>&nbsp;</p>
<p><a name="q3" id="q3"></a>Q: View Panel could not connect to the database...</p>
<p>A: This means what it says. To solve it try waiting about 10 minutes and trying again. If that fails then make sure the username and password you entered for your database settings are correct (you can check them in the settings page or in the settings MySQl table). If everything looks OK then contact someone on the forum at <a href="http://forum.StreetEye.info">forum.StreetEye.info</a></p>
<p>&nbsp;</p>
<p><a name="q4" id="q4"></a>Q: A value has not been established for View_Panel_MySQL_Connect().</p>
<p>A: This error is very old and dates back to version 1. Please update your panel and try again. If the problem persists then contact someone  on the forum at <a href="http://forum.StreetEye.info">forum.StreetEye.info</a>.</p>
<p>&nbsp;</p>
<p><a name="q5" id="q5"></a>Q: I've still got problems!</p>
<p>A: This guide is usually out of date and we know won't solve many problems. So, for any other problems use the forum. You can get there by clicking <a href="http://forum.StreetEye.info">here</a>. When posting your bug or error please include:</p>
<ul>
  <li>Your View Panel Version.</li>
  <li>The Error Itself.</li>
  <li>What plugins and theme you are using.</li>
  <li>What you were doing when the error occured.</li>
</ul>
<p>If you can't get this information (say you can't get into View Panel) then don't worry about it.</p>
