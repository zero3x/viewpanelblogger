<h1>Using HTML in Posts</h1>
<p>If you want to add headings, tables, lists... etc... to your post you'll need to use HTML. Here is a brief run down of the tags to use.</p>
<p>&nbsp;</p>
<h1>How To Use It</h1>
<p>It's very simple. Just type the opening tag, your text then the closing tag. Remember, every opening tag needs a close.</p>
<p>&nbsp;</p>
<h1>Headings</h1>
<h1>&lt;h1&gt;Your Text&lt;/h1&gt;</h1>
<h2>&lt;h2&gt;Your Text&lt;/h2&gt;</h2>
<h3>&lt;h3&gt;Your Text&lt;/h3&gt;</h3>
